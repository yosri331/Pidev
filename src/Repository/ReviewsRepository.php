class ReviewsRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Reviews::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Reviews $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Reviews $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }
    public function getReviews(int $id){
        $qb=$this->createQueryBuilder('e')
        ->where('e.event_id LIKE :id')
        ->andWhere('e.hidden LIKE :bool')
        ->setParameters(['id' => '%' . $id . '%','bool' => '%' . true . '%'])
        ->getQuery();
        return $qb->execute();
    }

